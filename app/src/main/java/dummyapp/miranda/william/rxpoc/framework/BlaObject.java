package dummyapp.miranda.william.rxpoc.framework;


public class BlaObject {

    private long id;

    private String name;

    public BlaObject() {

    }

    public BlaObject(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
